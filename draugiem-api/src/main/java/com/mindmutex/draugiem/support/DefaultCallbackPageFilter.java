package com.mindmutex.draugiem.support;

import java.io.IOException;
import java.io.InputStream;
import java.io.InputStreamReader;
import java.io.PrintWriter;

import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.FilterConfig;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;

/** 
 * Renders callback.html contents to the mapped filter target.
 * 
 * @author ivarsv
 */
public class DefaultCallbackPageFilter implements Filter {

	@Override
	public void init(FilterConfig filterConfig) throws ServletException {
	}

	@Override
	public void doFilter(ServletRequest request, ServletResponse response,
			FilterChain chain) throws IOException, ServletException {
		PrintWriter writer = response.getWriter();

		ClassLoader classLoader = getClass().getClassLoader();
		InputStream inputStream = classLoader.getResourceAsStream("META-INF/callback.html");

		InputStreamReader reader = new InputStreamReader(inputStream, "UTF-8");
		
		int read = 0; 
		while ((read = reader.read()) != -1) {
			writer.write(read);
		}
	}

	@Override
	public void destroy() {
	}
}
